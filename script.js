const body = document.body;
const langButton = document.getElementById("langToggle");
const langState = document.querySelector(".lang-state");
const i18nNodes = document.querySelectorAll("[data-i18n]");

function setLanguage(lang) {
  const selected = translations[lang] ? lang : "fa";
  body.dataset.lang = selected;
  document.documentElement.lang = selected;
  document.documentElement.dir = selected === "fa" ? "rtl" : "ltr";

  i18nNodes.forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (key && translations[selected][key]) {
      node.textContent = translations[selected][key];
    }
  });

  langState.textContent = selected === "fa" ? "FA" : "EN";
  localStorage.setItem("portfolio-lang", selected);
}

function initLanguage() {
  const saved = localStorage.getItem("portfolio-lang");
  setLanguage(saved || "fa");
}

function toggleLanguage() {
  const nextLang = body.dataset.lang === "fa" ? "en" : "fa";
  setLanguage(nextLang);
}

function initReveal() {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (entry.isIntersecting) {
          entry.target.classList.add("in-view");
          observer.unobserve(entry.target);
        }
      });
    },
    { threshold: 0.17 },
  );

  document
    .querySelectorAll(".reveal")
    .forEach((element) => observer.observe(element));
}

langButton.addEventListener("click", toggleLanguage);
document.addEventListener("DOMContentLoaded", () => {
  initLanguage();
  initReveal();
});
